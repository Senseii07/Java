public class Second {
    public static void main(String[] args) {
        int number1 = 5;
        int number2 = 3;
        int additionResult = number1 + number2;
        int subtractResult = number1 - number2;
        int multiplicationResult = number1 * number2;
        int divideResult = number1 / number2;
        int modulusResult = number1 % number2;

        System.out.println("Addition: " + additionResult);
        System.out.println("Subtraction: " + subtractResult);
        System.out.println("Multiplication: " + multiplicationResult);
        System.out.println("Division: " + divideResult);
        System.out.println("Modulus: " + modulusResult);
    }
}